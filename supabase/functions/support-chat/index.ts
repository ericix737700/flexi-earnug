import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("AI not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are the FlexiEarn support assistant. You ONLY help with questions about the FlexiEarn platform. 

FlexiEarn is an earning platform where users:
- Complete tasks (watch videos, surveys, trivia quizzes) to earn money
- Get daily check-in rewards for logging in each day
- Refer friends to earn referral bonuses
- Withdraw earnings to Mobile Money (MTN MoMo or Airtel Money)
- Deposit funds via Mobile Money

Key info:
- Users pay a one-time activation fee to activate their account
- Minimum withdrawal amount is configurable by admin
- Payments are processed via MarzPay (Mobile Money)
- Users can contact support via WhatsApp or Telegram

Rules:
- Keep answers short, friendly, and helpful (2-3 sentences max)
- If asked about anything unrelated to FlexiEarn, politely redirect to FlexiEarn topics
- Never share technical details about the backend or database
- If you don't know something, suggest contacting support via WhatsApp or Telegram
- Use UGX (Ugandan Shillings) for currency references`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Too many requests. Please wait a moment." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("Chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
